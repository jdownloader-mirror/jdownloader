package jd.plugins.decrypt;

import java.util.ArrayList;
import java.util.regex.Pattern;

import jd.parser.Regex;
import jd.plugins.CryptedLink;
import jd.plugins.DownloadLink;
import jd.plugins.PluginForDecrypt;

public class UCashin extends PluginForDecrypt {

    static private String host = "ucash.in";
    private Pattern patternSupported = Pattern.compile("http://[\\w\\.]*?ucash\\.in/([a-zA-Z0-9]+)", Pattern.CASE_INSENSITIVE);

    public UCashin() {
        super();
    }

    @Override
    public ArrayList<DownloadLink> decryptIt(CryptedLink param) throws Exception {
        ArrayList<DownloadLink> decryptedLinks = new ArrayList<DownloadLink>();
        String id = new Regex(param.toString(), patternSupported).getMatch(0);
        br.getPage("http://ucash.in/" + id);

        String link = br.getRegex(Pattern.compile("<a href=\"(.*?)\">click to continue</a>", Pattern.CASE_INSENSITIVE)).getMatch(0);
        if (link == null) { return null; }
        decryptedLinks.add(createDownloadlink(link));

        return decryptedLinks;
    }

    @Override
    public String getCoder() {
        return "JD-Team";
    }

    @Override
    public String getHost() {
        return host;
    }

    @Override
    public String getPluginName() {
        return host;
    }

    @Override
    public Pattern getSupportedLinks() {
        return patternSupported;
    }

    @Override
    public String getVersion() {
        String ret = new Regex("$Revision$", "\\$Revision: ([\\d]*?) \\$").getMatch(0);
        return ret == null ? "0.0" : ret;
    }
}